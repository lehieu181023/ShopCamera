// Toast dạng SweetAlert2
const Toast = Swal.mixin({
    toast: true,
    position: 'top-end',
    showConfirmButton: false,
    timer: 3000,
    timerProgressBar: true
});
function confirmDelete(message = 'Bạn có chắc chắn muốn xóa không?', callback) {
    Swal.fire({
        title: 'Xác nhận',
        text: message,
        icon: 'warning',
        showCancelButton: true,
        confirmButtonColor: '#d33',
        cancelButtonColor: '#3085d6',
        confirmButtonText: 'Xóa',
        cancelButtonText: 'Hủy'
    }).then((result) => {
        if (result.isConfirmed) {
            callback();
        }
    });
}
function showToast(message, type = 'success') {
    let bgColor;
    switch (type) {
        case 'success': bgColor = '#4caf50'; break;
        case 'error': bgColor = '#f44336'; break;
        case 'warning': bgColor = '#ff9800'; break;
        default: bgColor = '#2196f3'; // info
    }

    Toast.fire({
        icon: type,
        title: message,
        background: bgColor,
        color: '#fff'
    });
}
function BlockUI(message, element) {
    debugger;
    var text = message || "Loading...";
    var $target = element ? $(element) : $("body"); // nếu không có element → block toàn trang

    $target.LoadingOverlay("show", {
        text: text,
        spinner: true,            // dùng spinner mặc định
        spinnerSize: "large",         // to hơn
        spinnerColor: "#000",          // màu spinner
        background: "rgba(255,255,255,0.6)" // nền overlay
    });
}

// Hàm Unblock UI
function UnBlockUI(element) {
    var $target = element ? $(element) : $("body");
    $target.LoadingOverlay("hide");
}
